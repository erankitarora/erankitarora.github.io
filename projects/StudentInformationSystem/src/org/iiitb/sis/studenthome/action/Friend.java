package org.iiitb.sis.studenthome.action;

public class Friend {
	private int sno;
	private String fname;
	private String frollno;
	private String sem;
	private int sid;
	public String getSem() {
		return sem;
	}
	public void setSem(String sem) {
		this.sem = sem;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFrollno() {
		return frollno;
	}
	public void setFrollno(String frollno) {
		this.frollno = frollno;
	}
	public int getSno() {
		return sno;
	}
	public void setSno(int count) {
		this.sno = count;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}	
}
